    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
         DefaultListModel<Species> s = new DefaultListModel<>();
         s.addAll(processor.getData(txtCondition.getText()));
         lstSpecies.setModel(s);
    }//GEN-LAST:event_btnSearchActionPerformed

     private void loadData(){
       
       DefaultListModel<Species> sp = new DefaultListModel<>();
        
             
       processor.getData().forEach(g -> {
           sp.addElement(g);
       });
       

        lstSpecies.setModel(sp);
       
   }
   
   private void setValuetoEntity(){
       
       
       var entity =processor.getEntity();      
       
            
       entity.setName(txtName.getText());
      
 
        
            
   }
   
    private void cleanUp(){
       
       txtName.setText("");
       txtCondition.setText("");
       
   
   
   }
    

